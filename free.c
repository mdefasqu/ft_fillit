#include "fillit.h"

void	free_final_tab(int ****tab, int len)
{
	int i;
	int y;

	i = 1;
	while (i < len)
	{
		y = 0;
		while (y < 3)
		{
			free((*tab)[i][y]);
			(*tab)[i][y] = NULL;
			y++;
		}
		i++;
	}
}
